<h3 class="collapse">Unable to locate a Wi-Fi network</h3>
<div>
<ol>
    <li>Verify that the network is available by tapping <strong>Settings &gt; Wi-Fi</strong> and choosing from the available networks. <strong>Note:</strong> It may take a few seconds for the Wi-Fi network name to appear.</li>
    <li>Move closer to your wireless router (access point) and attempt to locate the Wi-Fi network.</li>
    <li>If you do not see the network you would like to join, you may be attempting to connect to a hidden network.  Learn <a href="http://support.apple.com/kb/HT5569" onclick="s_objectID=&quot;http://support.apple.com/kb/HT5569_1&quot;;return this.s_oc?this.s_oc(e):true">how to join a hidden network</a>.</li>
    <li>Supported Wi-Fi configurations vary by iOS   device model.  Find out <a href="http://support.apple.com/specs/" onclick="s_objectID=&quot;http://support.apple.com/specs/_1&quot;;return this.s_oc?this.s_oc(e):true">which standards</a> your device supports<sup><a href="#footnote" onclick="s_objectID=&quot;http://support.apple.com/en-us/TS1398#footnote_4&quot;;return this.s_oc?this.s_oc(e):true">3</a></sup>.</li>
    <li>Reset network settings by tapping <strong>Settings &gt; General &gt; Reset &gt; Reset Network Settings</strong>. <strong>Note:</strong> This will reset all network settings including:
    <ul>
        <li>previously connected Wi-Fi networks and passwords</li>
        <li>recently used Bluetooth accessories</li>
        <li>VPN and APN settings</li>
    </ul>
    </li>
</ol>
</div>
<h3 class="collapse">Unable to connect to a Wi-Fi network</h3>
<div>
<ol>
    <li>Verify that  you're attempting to connect to your desired Wi-Fi network.</li>
    <li>Make sure you're entering your Wi-Fi password correctly. Passwords may be case sensitive and may contain numbers or special characters.</li>
    <li>Reset network settings by tapping <strong>Settings &gt; General &gt; Reset &gt; Reset Network Settings</strong>. <strong>Note:</strong> This will reset all network settings including:
    <ul>
        <li>previously connected Wi-Fi networks and passwords</li>
        <li>recently used Bluetooth accessories</li>
        <li>VPN and APN settings</li>
    </ul>
    </li>
</ol>
</div>
<h3 class="collapse">Wi-Fi disconnects or signal strength is less than expected</h3>
<div>
<ol>
    <li>Move closer to the Wi-Fi router (access point).</li>
    <li>Check for sources of <a href="http://support.apple.com/kb/HT1365" onclick="s_objectID=&quot;http://support.apple.com/kb/HT1365_1&quot;;return this.s_oc?this.s_oc(e):true">potential interference</a>.</li>
    <li>Remove any case, stand, or other accessories from your iOS device and see if signal strength improves.</li>
    <li>Reset network settings by tapping <strong>Settings &gt; General &gt; Reset &gt; Reset Network Settings</strong>. <strong>Note:</strong> This will reset all network settings including
    <ul>
        <li>previously connected Wi-Fi networks and passwords</li>
        <li>recently used Bluetooth accessories</li>
        <li>VPN and APN settings</li>
    </ul>
    </li>
</ol>
</div>
<h3 class="collapse">Unable to access the Internet while connected to Wi-Fi</h3>
<div>
<ol>
    <li>If you're using a public or commercial network, you may need to <a href="http://support.apple.com/kb/HT3867" onclick="s_objectID=&quot;http://support.apple.com/kb/HT3867_1&quot;;return this.s_oc?this.s_oc(e):true">log in or subscribe</a>.</li>
    <li>Check to see if you have a self-assigned IP address:
    <ol type="a">
        <li>Tap <strong>Settings &gt; Wi-Fi</strong> and locate the Wi-Fi network to which you're connected and tap <img width="24" height="22" align="absBottom" src="http://km.support.apple.com/library/APPLE/APPLECARE_ALLGEOS/TS1398/TS1398_i.png" alt="" data-hires="true">.</li>
        <li>If your IP   address is 169.254.xxx.xxx, <a href="http://support.apple.com/kb/TS3798" onclick="s_objectID=&quot;http://support.apple.com/kb/TS3798_1&quot;;return this.s_oc?this.s_oc(e):true">you may not be able to access the Internet</a>.</li>
    </ol>
    </li>
    <li>If you do not have a self-assigned IP address, see if other Wi-Fi  devices on your network have Internet access.
    <ul>
        <li>If they do not, consult with your network administrator or ISP for further assistance.</li>
        <li>If they do, reset network settings on your device by tapping <strong>Settings &gt; General &gt; Reset &gt; Reset Network Settings</strong>. <strong>Note:</strong> This will reset all network settings including:
        <ul>
            <li>previously connected Wi-Fi networks and passwords</li>
            <li>recently used Bluetooth accessories</li>
            <li>VPN and APN settings</li>
        </ul>
        </li>
    </ul>
    </li>
</ol>
</div>
<h3 class="collapse">Unable to automatically connect to Wi-Fi</h3>
<div>
<ul>
    <li><a href="http://support.apple.com/kb/HT4450" onclick="s_objectID=&quot;http://support.apple.com/kb/HT4450_1&quot;;return this.s_oc?this.s_oc(e):true">Avoid giving your Wi-Fi network a common name</a>.
    <ul>
        <li>Some wireless routers are preconfigured with a common SSID (network name) such as "wireless" or "default."</li>
        <li>If your desired Wi-Fi network has a common SSID, try changing the SSID to something unique<sup><a href="#footnote" onclick="s_objectID=&quot;http://support.apple.com/en-us/TS1398#footnote_5&quot;;return this.s_oc?this.s_oc(e):true">2</a></sup>.</li>
    </ul>
    </li>
    <li>If your desired Wi-Fi network has a unique SSID, reset network settings by tapping <strong>Settings &gt; General &gt; Reset &gt; Reset Network Settings</strong>. <strong>Note:</strong> This will reset all network settings including:
    <ul>
        <li>previously connected Wi-Fi networks and passwords</li>
        <li>recently used Bluetooth accessories</li>
        <li>VPN and APN settings</li>
    </ul>
    </li>
</ul>
</div></section><section class=""><ul>
    <li>Find information on <a href="https://support.apple.com/kb/HT5569" onclick="s_objectID=&quot;https://support.apple.com/kb/HT5569_1&quot;;return this.s_oc?this.s_oc(e):true">connecting to a Wi-Fi network</a><a href="https://support.apple.com/kb/HT5569" onclick="s_objectID=&quot;https://support.apple.com/kb/HT5569_2&quot;;return this.s_oc?this.s_oc(e):true">.</a></li>
    <li>Some third-party routers may not be compatible with all iOS devices, even after updating to the latest firmware. If your iOS  devices can successfully connect to other Wi-Fi networks, contact your  router's manufacturer to ensure that your router is compatible with your iOS device.</li>
    <li>See recommended settings for <a href="http://support.apple.com/kb/HT4199" onclick="s_objectID=&quot;http://support.apple.com/kb/HT4199_1&quot;;return this.s_oc?this.s_oc(e):true">configuring your Wi-Fi router for use with iOS devices</a>.</li>
</ul>
