<h1 id="main-title" itemprop="headline">iOS: Troubleshooting Wi-Fi networks and connections</h1>
<div class="intro"><p>Learn how to troubleshoot Wi-Fi<sup><a href="#footnote" onclick="s_objectID=&quot;http://support.apple.com/en-us/TS1398#footnote_1&quot;;return this.s_oc?this.s_oc(e):true">1</a></sup> issues you may encounter while using your iPhone, iPad, or iPod touch.</p></div>

<div itemprop="author" itemscope="" itemtype="http://schema.org/Organization">
  <meta itemprop="name" content="Apple">
</div>
<div id="sections" itemprop="articleBody">
<section class=""><p>Occasionally while using your iOS device, you may notice unexpected Wi-Fi behavior:</p>
<ul>
    <li>Difficulty locating or connecting to a network</li>
    <li>Unexpected Wi-Fi signal strength or disconnects</li>
    <li>Inability to  access the Internet</li>
    <li>Not automatically connecting to Wi-Fi when expected</li>
</ul>
<p>Follow these steps to troubleshoot the above issues:</p>
<ol>
    <li>Be sure you're in range of your Wi-Fi router (access point).</li>
    <li>Tap <strong>Settings &gt; Wi-Fi</strong> and turn Wi-Fi off and on. If your Wi-Fi setting is dimmed, <a href="http://support.apple.com/kb/TS1559" onclick="s_objectID=&quot;http://support.apple.com/kb/TS1559_1&quot;;return this.s_oc?this.s_oc(e):true">follow these steps</a>.</li>
    <li>Confirm that your Wi-Fi router and cable or DSL modem are connected to power, turned on, and connected to the Internet. If not, refer to your network administrator or Internet service provider (ISP) for assistance.</li>
    <li><a href="http://support.apple.com/kb/HT1430" onclick="s_objectID=&quot;http://support.apple.com/kb/HT1430_1&quot;;return this.s_oc?this.s_oc(e):true">Restart your iOS device</a>.</li>
    <li>Tap <strong>Settings &gt; Wi-Fi</strong> and locate the Wi-Fi network to which you're  connected.</li>
    <li>Tap <img width="24" height="22" align="absBottom" alt="" src="http://km.support.apple.com/library/APPLE/APPLECARE_ALLGEOS/TS1398/TS1398_i.png" data-hires="true"> and <strong>Forget this Network</strong>.</li>
    <li>Try to connect to your desired Wi-Fi network. <br>
    <strong>Note</strong>: You may need to enter your Wi-Fi password again if your network requires one.</li>
    <li>Turn your Wi-Fi router  off and on<sup><a href="#footnote" onclick="s_objectID=&quot;http://support.apple.com/en-us/TS1398#footnote_2&quot;;return this.s_oc?this.s_oc(e):true">2</a></sup>.  If your ISP also provides cable or phone service, check with them before attempting this step to avoid interruption of service.</li>
    <li><a href="http://support.apple.com/kb/HT4623" onclick="s_objectID=&quot;http://support.apple.com/kb/HT4623_1&quot;;return this.s_oc?this.s_oc(e):true">Update your device</a> to the latest version of software.</li>
    <li>Update your Wi-Fi router to the latest firmware<sup><a href="#footnote" onclick="s_objectID=&quot;http://support.apple.com/en-us/TS1398#footnote_3&quot;;return this.s_oc?this.s_oc(e):true">2</a></sup>. For AirPort Base Stations, <a href="http://support.apple.com/kb/HT1218" onclick="s_objectID=&quot;http://support.apple.com/kb/HT1218_1&quot;;return this.s_oc?this.s_oc(e):true">install updates using the AirPort Utility</a>.</li>
</ol>
<h2>If the issue is still unresolved</h2>
<p>Follow the steps below for the issue you're experiencing.</p>
